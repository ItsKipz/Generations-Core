	private static RegistrySupplier<Block> registerBlockItem(String name, boolean shiny) {
		return registerBlockItem(name, shiny, 1.0f);
	}

	private static RegistrySupplier<Block> registerBlockItem(String name, boolean shiny, float scale) {
		RegistrySupplier<Block> block = POKEDOLLS.register((shiny ? "shiny_" : "") + name  + "_doll", () -> new PokeDollBlock(name, shiny, scale));
		GenerationsItems.ITEMS.register((shiny ? "shiny_" : "") + name  + "_doll", () -> new BlockItem(block.get(), new Item.Properties().arch$tab(GenerationsCreativeTabs.DECORATION_BLOCKS)));
		return block;
	}

